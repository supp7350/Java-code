class Demo3i{
	public static void main(String []  args){
		String ch ="S";

		switch(ch){
			case "S":
				System.out.println("Small");
				break;
			case "M":
				System.out.println("Medium");
				break;
			case "L":
				System.out.println("Large");
				break;
			case "XL":
				System.out.println("Extra Large");	
				break;
			default:
				System.out.println("Exta Extra Large");
		}
	}
}


