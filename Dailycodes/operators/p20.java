class bitwiseoperator{
        public static void main(String[] args){
                int x = 35;    //binary = 0010 1011
                int y = 21;    //binary = 0001 0101
                System.out.println(x & y);    //1 i.e.0000 0001
		System.out.println(x | y);    //55 i.e.0011 0111
        }
}
