class IfElseDemo{
        public static void main(String[] args){
                int num = -8;
                System.out.println("START CODE");
                if(num>0){
                        System.out.println("Number is positive");
		}else if(num<0){
                        System.out.println("Number is negative");
                }else{
                        System.out.println("Number is zero");
                }
                System.out.println("END CODE");
        }
}
