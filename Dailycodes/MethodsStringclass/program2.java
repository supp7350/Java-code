class ConcatStringDemo{
        public static void main(String[] args){
                String str1 = "Shashi";
                String str2 = "Bagal";
                System.out.println(str1);
                System.out.println(str2);

                System.out.println(str1.concat(str2));

                System.out.println(str1);
                System.out.println(str2);
        }
}  
